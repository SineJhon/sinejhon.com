function renderProjects() {
    const root = document.getElementById('projectShowcaseList');
    if (!root) {
        return;
    }

    root.innerHTML = projectData
        .map((project, projectIndex) => {
            const trackId = `projectTrack${projectIndex + 1}`;
            const counterId = `projectCounter${projectIndex + 1}`;

            const slides = project.photos
                .map(
                    (photo) =>
                        `<figure class="project-slide"><img src="${photo.src}" alt="${photo.alt}" loading="lazy"></figure>`
                )
                .join('');

            return `
                <article class="project-showcase-card" data-project-card>
                    <div class="project-carousel" data-carousel>
                        <button type="button" class="project-carousel-button prev" data-direction="prev" aria-label="Previous photo">&#8249;</button>
                        <div class="project-carousel-viewport">
                            <div class="project-carousel-track" id="${trackId}" data-track>
                                ${slides}
                            </div>
                        </div>
                        <button type="button" class="project-carousel-button next" data-direction="next" aria-label="Next photo">&#8250;</button>
                        <p class="project-carousel-counter" id="${counterId}" data-counter>1 / ${project.photos.length}</p>
                    </div>

                    <header class="project-showcase-head">
                        <h3 class="project-showcase-title">${project.title}</h3>
                        <p class="project-showcase-type">Type: ${project.type}</p>
                        <p class="project-showcase-models">${project.models}</p>
                        <blockquote class="project-showcase-quote">"${project.quote}"</blockquote>
                    </header>
                </article>
            `;
        })
        .join('');
}

function setupCarousels() {
    const carousels = document.querySelectorAll('[data-carousel]');

    carousels.forEach((carousel) => {
        const track = carousel.querySelector('[data-track]');
        const counter = carousel.querySelector('[data-counter]');
        const slides = track ? track.children : [];

        if (!track || slides.length === 0) {
            return;
        }

        let currentIndex = 0;

        function updateCarousel() {
            track.style.transform = `translateX(-${currentIndex * 100}%)`;
            counter.textContent = `${currentIndex + 1} / ${slides.length}`;
        }

        carousel.querySelectorAll('.project-carousel-button').forEach((button) => {
            button.addEventListener('click', () => {
                const direction = button.getAttribute('data-direction');

                if (direction === 'next') {
                    currentIndex = (currentIndex + 1) % slides.length;
                } else {
                    currentIndex = (currentIndex - 1 + slides.length) % slides.length;
                }

                updateCarousel();
            });
        });

        updateCarousel();
    });
}

renderProjects();
setupCarousels();
